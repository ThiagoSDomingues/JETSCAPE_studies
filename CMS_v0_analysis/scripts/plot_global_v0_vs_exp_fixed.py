# CENTRALITY BIN ALIGNMENT
# =============================================================================

def remap_predictions_to_correct_centrality(pred_data):
    """
    Remap prediction centrality centers to the correct ATLAS bin centers.

    The predictions file may have stored wrong centrality centers (e.g. starting
    at 0 instead of 2.5, or using uniform 10%-wide bins instead of the actual
    non-uniform ATLAS bins).  We assume there is a one-to-one correspondence
    between the N prediction bins and the N correct centrality bins and simply
    replace the stored centers with CENTRALITY_CENTERS_CORRECT.

    If the number of prediction bins does not match, we fall back to linear
    interpolation onto the correct grid.
    """
    stored_centers = np.asarray(pred_data['centrality_centers'])
    n_pred = len(stored_centers)
    n_correct = len(CENTRALITY_CENTERS_CORRECT)

    if n_pred == n_correct:
        print(f"    Remapping {n_pred} prediction bins:")
        print(f"      Stored centers : {stored_centers}")
        print(f"      Correct centers: {CENTRALITY_CENTERS_CORRECT}")
        # Direct replacement — bin i of predictions corresponds to bin i of ATLAS
        remapped = pred_data.copy()
        remapped['centrality_centers'] = CENTRALITY_CENTERS_CORRECT.copy()
        return remapped
    else:
        print(f"    WARNING: {n_pred} prediction bins vs {n_correct} correct bins.")
        print(f"    Falling back to interpolation onto correct centrality grid.")
        remapped = pred_data.copy()
        for key in ('prior', 'original_posterior', 'posterior'):
            arr = pred_data.get(key)
            if arr is not None:
                arr_new = np.zeros((arr.shape[0], n_correct))
                for s in range(arr.shape[0]):
                    arr_new[s, :] = np.interp(
                        CENTRALITY_CENTERS_CORRECT, stored_centers, arr[s, :]
                    )
                remapped[key] = arr_new
        remapped['centrality_centers'] = CENTRALITY_CENTERS_CORRECT.copy()
        return remapped


def clip_predictions_to_exp_range(pred_data, exp_data):
    """
    Return a view of pred_data restricted to centrality bins that fall within
    the experimental centrality range [exp_min, exp_max].
    Clips prior, original_posterior, and posterior arrays.
    """
    cent = np.asarray(pred_data['centrality_centers'])
    exp_max = exp_data['centrality'].max()
    exp_min = exp_data['centrality'].min()

    mask = (cent >= exp_min) & (cent <= exp_max)
    clipped = pred_data.copy()
    clipped['centrality_centers'] = cent[mask]
    for key in ('prior', 'original_posterior', 'posterior'):
        if clipped.get(key) is not None:
            clipped[key] = clipped[key][:, mask]
    return clipped


